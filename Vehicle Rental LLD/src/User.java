public class User {

    int userId;
    String name;
    int drivingLicens;

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDrivingLicens() {
        return drivingLicens;
    }

    public void setDrivingLicens(int drivingLicens) {
        this.drivingLicens = drivingLicens;
    }
}
